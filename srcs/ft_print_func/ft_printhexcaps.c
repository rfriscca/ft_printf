#include "ft_printf.h"

void	ft_printhexcaps(uintmax_t nbr, t_stock *stock)
{
	int		spacenum;
	int		size;

	spacenum = 0;
	size = ft_nbrsizehex(nbr);
	if (stock->size_min > size && stock->size_min <= 2147483647)
		spacenum = stock->size_min - size;
	if ((stock->flags & 16) != 0)
		spacenum -= 2;
	if ((stock->flags & 1) == 0)
	{
		if ((stock->flags & 2) == 0)
			ft_putspace(spacenum);
		else
			ft_putzero(spacenum, stock, 'X');
		if ((stock->flags & 2) == 0)
			ft_printadd(stock, 'X');
		ft_converttohex(nbr, 1);
	}
	else
	{
		ft_printadd(stock, 'X');
		ft_converttohex(nbr, 1);
		ft_putspace(spacenum);
	}
	stock->i += spacenum + size;
}
