#include "ft_printf.h"

void	ft_putunbr(uintmax_t n)
{
	if (n > 9)
	{
		ft_putunbr(n / 10);
		ft_putunbr(n % 10);
	}
	if (n <= 9)
		ft_putchar(n + 48);
}
