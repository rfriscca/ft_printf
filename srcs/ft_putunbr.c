#include "ft_printf.h"

void	ft_putunbr(unsigned int n)
{
	unsigned int	n2;

	n2 = n;
	if (n2 > 9)
	{
		ft_putunbr(n2 / 10);
		ft_putunbr(n2 % 10);
	}
	if (n2 <= 9)
		ft_putchar(n2 + 48);
}
